package claseString;
public class claseee {
    public static void main(String[] args) {
        String str ="Alexander";
        System.out.println(str.length());
        System.out.println(str.indexOf("a"));
        System.out.println(str.lastIndexOf("x"));
        System.out.println(str.charAt(4));
        System.out.println(str.subSequence(2, 4));
        System.out.println(str.toUpperCase());
        System.out.println(str.toLowerCase());
        System.out.println(str.equals("alexander"));
        System.out.println(str.equalsIgnoreCase("ALEXander"));
        
        
        
    }
}
